export function getLevelProgress(experience: number, level: number) {
  if (!experience || !level) return 0;
  if (level == 100) return 100;
  const currLevelInfo =
    Levels.find((info) => info.level == level) ??
    ({ level: 0, exp: 0, expToLevel: 0 } as LevelInfo);
  return ((experience - currLevelInfo.exp) / currLevelInfo.expToLevel) * 100;
}
